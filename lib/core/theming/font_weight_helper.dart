import 'package:flutter/material.dart';

/// `FontWeightHelper` is a utility class that provides a centralized place to manage font weights used in the application.
///
/// It defines eight font weights: thin, extraLight, light, regular, medium, semiBold, bold, and extraBold.
class FontWeightHelper {
  // The thin font weight (FontWeight.w100).
  static const FontWeight thin = FontWeight.w100;

  // The extra light font weight (FontWeight.w200).
  static const FontWeight extraLight = FontWeight.w200;

  // The light font weight (FontWeight.w300).
  static const FontWeight light = FontWeight.w300;

  // The regular font weight (FontWeight.w400).
  static const FontWeight regular = FontWeight.w400;

  // The medium font weight (FontWeight.w500).
  static const FontWeight medium = FontWeight.w500;

  // The semi-bold font weight (FontWeight.w600).
  static const FontWeight semiBold = FontWeight.w600;

  // The bold font weight (FontWeight.w700).
  static const FontWeight bold = FontWeight.w700;

  // The extra bold font weight (FontWeight.w800).
  static const FontWeight extraBold = FontWeight.w800;
}